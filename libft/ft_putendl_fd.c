#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i++])
	{
		ft_putchar_fd(s[i - 1], fd);
	}
	ft_putchar_fd('\n', fd);
}
